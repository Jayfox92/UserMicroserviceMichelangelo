package com.michelangelo.usermicroservice.VO;

public class GenreVO {
    private Long id;
    private String name;
    private int count;

    public GenreVO() {
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getId() {
        return id;
    }
}
